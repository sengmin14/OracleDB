-- 10000건 데이터 삽입 (JOB 다양화)
DECLARE
    v_job_list SYS.ODCIVARCHAR2LIST := SYS.ODCIVARCHAR2LIST(
        'ANALYST','CLERK','MANAGER','SALESMAN','PRESIDENT',
        'ENGINEER','DEVELOPER','DBA','DESIGNER','TESTER',
        'CONSULTANT','TRAINER','ARCHITECT','PLANNER','SUPPORT'
    );
    v_cnt NUMBER := 10000;
BEGIN
    FOR i IN 1..v_cnt LOOP
        INSERT INTO EMP (
            EMPNO, ENAME, JOB, MGR, HIREDATE, SAL, COMM, DEPTNO
        ) VALUES (
            10000 + i,
            'EMP'||i,
            v_job_list(TRUNC(DBMS_RANDOM.VALUE(1, v_job_list.COUNT+1))),
            NULL,
            TRUNC(SYSDATE - DBMS_RANDOM.VALUE(0, 5000)),
            TRUNC(DBMS_RANDOM.VALUE(1000, 5000)),
            CASE WHEN MOD(i, 5) = 0 THEN TRUNC(DBMS_RANDOM.VALUE(100, 1000)) END,
            CASE WHEN MOD(i, 4) = 0 THEN 10
                 WHEN MOD(i, 4) = 1 THEN 20
                 WHEN MOD(i, 4) = 2 THEN 30
                 ELSE 40 END
        );
        IF MOD(i, 1000) = 0 THEN
            COMMIT; -- 대량 처리 시 중간 커밋
        END IF;
    END LOOP;
    COMMIT;
END;
/

-- 건수 확인
SELECT COUNT(*) AS total_rows FROM EMP;
SELECT JOB, COUNT(*) FROM EMP GROUP BY JOB ORDER BY COUNT(*) DESC;
SELECT DEPTNO, COUNT(*) FROM EMP GROUP BY DEPTNO ORDER BY DEPTNO;

